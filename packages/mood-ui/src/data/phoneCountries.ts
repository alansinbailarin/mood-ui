/** Find a country by ISO 3166-1 alpha-2 code (case-insensitive). */
export function findCountryByIso(iso: string): PhoneCountry | undefined {
    return PHONE_COUNTRIES.find((c) => c.iso === iso.toUpperCase());
}

/** Find countries by dial code (may return multiple, e.g. +1 → US + CA). */
export function findCountriesByDial(dial: string): PhoneCountry[] {
    const clean = dial.replace(/^\+/, '');
    return PHONE_COUNTRIES.filter((c) => c.dial === clean);
}
